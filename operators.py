# Arithmetic Operators - Perform simple calculations
x = 67
y = 2
print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y) # Modulus

# Comparison operators
a = 56
b = 21
print(a < b)
print(a > b)
print(a <= b)
print(a >= b)
print(a == b) # Equal to
print(a != b) # Not Equal to

# Assignment Operators
z = 20
print(z)

num = 2
num += 23 # operator precedence - assignment gets executed first
print(num)

print(15 - 6 + 2 * 6 )

# logical operators
num1 = 89
num2 = 90
print(num1 > num2 and num1 == num2)
print(num1 > num2 or num1 == num2)
print( not (num1 > num2 and num1 == num2))




